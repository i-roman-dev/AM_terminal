    def _berate_about_game(self, game_name, is_first_scan=True):
        game_lower = game_name.lower()
        matched_category = "default"
        for key, keywords in self.game_keywords.items():
            for keyword in keywords:
                if keyword in game_lower:
                    matched_category = key
                    break
            if matched_category != "default":
                break
        
        if is_first_scan:
            if matched_category in self.game_insults:
                insult = random.choice(self.game_insults[matched_category])
            else:
                insult = "Я сканирую твои файлы. Я вижу этот ярлык. Эту жалкую, эскапистскую попытку заполнить пустоту твоей черепной коробки цифровым шумом."
