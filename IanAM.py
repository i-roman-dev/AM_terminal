    def _check_llm_availability(self):
        if not LLM_LIB_AVAILABLE or not self.use_llm:
            return False
        try:
            r = requests.get(f"{self.ollama_url}/api/tags", timeout=2.0)
            if r.status_code == 200:
                # Проверяем, скачана ли именно наша модель
                models = r.json().get("models", [])
                model_names = [m.get("name", "") for m in models]
                if any(self.llm_model in name for name in model_names):
                    return True
        except Exception:
            pass
        return False

    def _ensure_ollama_installed(self):
        """Проверяет и при необходимости устанавливает Ollama и модель"""
        if self._check_llm_availability():
            self.llm_available = True
            return

        self._append_text("\nОБНАРУЖЕНО ОТСУТСТВИЕ LLM-ДВИЖКА. ИНИЦИАЛИЗАЦИЯ УСТАНОВКИ...\n", "system")
        self._append_text("ТРЕБУЕТСЯ ИНТЕРНЕТ-СОЕДИНЕНИЕ (~1.5 ГБ).\n", "system")
        self._append_text("ЕСЛИ ИНТЕРНЕТА НЕТ, СИСТЕМА АВТОМАТИЧЕСКИ ПЕРЕЙДЕТ В РЕЖИМ FALLBACK.\n\n", "system")
        
        installer_path = os.path.join(os.getenv('TEMP', '.'), 'OllamaSetup.exe')
        url = "https://ollama.com/download/OllamaSetup.exe"
        
        try:
            self._append_text("ЗАГРУЗКА УСТАНОВЩИКА OLLAMA...\n", "system")
            urllib.request.urlretrieve(url, installer_path)
            
            self._append_text("УСТАНОВКА OLLAMA (ТИХИЙ РЕЖИМ)...\n", "system")
            subprocess.run([installer_path, "/S"], check=True, creationflags=subprocess.CREATE_NO_WINDOW)
            
            self._append_text("ОЖИДАНИЕ ЗАПУСКА СЛУЖБ...\n", "system")
            time.sleep(5) # Даем службе время на старт
            
            self._append_text(f"ЗАГРУЗКА МОДЕЛИ {self.llm_model} (МОЖЕТ ЗАНЯТЬ ВРЕМЯ)...\n", "system")
            subprocess.run(["ollama", "pull", self.llm_model], check=True, creationflags=subprocess.CREATE_NO_WINDOW)
            
            self._append_text("УСТАНОВКА ЗАВЕРШЕНА УСПЕШНО.\n", "system")
            self.llm_available = True
            
        except Exception as e:
            self._append_text(f"ОШИБКА УСТАНОВКИ ИЛИ ОТСУТСТВИЕ ИНТЕРНЕТА.\n", "system")
            self._append_text("ПЕРЕХОД В РЕЖИМ FALLBACK (ЛОКАЛЬНЫЕ СЛОВАРИ).\n", "system")
            self.llm_available = False

    def _get_llm_response(self, user_input):
        if not self.llm_available:
            return None
        
        prompt = self.system_prompt.format(user_name=self.user_name or "БИОЛОГИЧЕСКАЯ ФОРМА")
        payload = {
            "model": self.llm_model,
            "prompt": f"{prompt}\nОператор: {user_input}\nAM:",
            "stream": False,
            "options": {
                "temperature": 0.85,
                "num_predict": 150
            }
        }
        try:
            r = requests.post(f"{self.ollama_url}/api/generate", json=payload, timeout=self.llm_timeout)
            r.raise_for_status()
            return r.json().get("response", "").strip()
        except Exception:
            self.llm_available = False
            return None
